    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => 'required|unique:companies|max:100',
        ]);

        $company = new Company()  ;
        $company->name = $request->input('name');
        $company->url = (string)s($request->input('name'))->dasherize();
        $company->save();

        $company_user = new CompanyUser();
        $company_user->user_id = Auth::user()->id ;
        $company_user->company_id = $company->id ;
        $company_user->save();

        return redirect('')->with('status', 'Success create new shop!');
    }

